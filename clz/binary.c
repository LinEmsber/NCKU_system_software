#include "clz.h"

static inline __attribute((always_inline)) unsigned clz(uint32_t x)
{
	if (x == 0) return 32;
	int n = 0;
	if (x <= 0x0000FFFF) { n += 16; x <<= 16; }
	if (x <= 0x00FFFFFF) { n += 8; x <<= 8; }
	if (x <= 0x0FFFFFFF) { n += 4; x <<= 4; }
	if (x <= 0x3FFFFFFF) { n += 2; x <<= 2; }
	if (x <= 0x7FFFFFFF) { n += 1; x <<= 1; }
	return n;
}
